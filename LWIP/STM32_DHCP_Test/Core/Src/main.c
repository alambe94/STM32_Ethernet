void print_ip(unsigned int ip)
{
    char buff[20] = {0};
    unsigned char bytes[4];
    bytes[3] = ip & 0xFF;
    bytes[2] = (ip >> 8) & 0xFF;
    bytes[1] = (ip >> 16) & 0xFF;
    bytes[0] = (ip >> 24) & 0xFF;
    sprintf(buff,"%d.%d.%d.%d\n", bytes[3], bytes[2], bytes[1], bytes[0]);
    HAL_UART_Transmit(&huart6, buff, strlen(buff), 1000);
}
